function start() {
    const url = decodeURIComponent(document.baseURI);
    const regex = /(teams\.html)\?(.+)/;
    const brandName = url.match(regex)[2] || "";
    const elem = document.getElementById("brand-content");

    const children = keyboards[brandName].map((item) => {
        const girdItem = document.createElement("div");
        girdItem.className = "grid-item";
        const imgWrapper = document.createElement("div");
        const img = document.createElement("img");
        img.src = item.image;
        imgWrapper.appendChild(img);
        const para1 = document.createElement("p");
        para1.textContent = item.teamname;
        const para2 = document.createElement("p");
        para2.textContent = item.top;
        const para3 = document.createElement("p");
        para3.textContent = item.jug;
        const para4 = document.createElement("p");
        para4.textContent = item.mid;
        const para5 = document.createElement("p");
        para5.textContent = item.adc;
        const para6 = document.createElement("p");
        para6.textContent = item.sup;

        const rightCol = document.createElement("div");
        rightCol.className = "product-right-col";
        [para1, para2, para3, para4, para5, para6].forEach((val) => rightCol.appendChild(val));
        [imgWrapper, rightCol].forEach((val) => girdItem.appendChild(val));
        return girdItem;
    });

    children.forEach((item) => elem.appendChild(item));
}

start();